  id: handle ? `${source}-${handle}-${target}` : `${source}-to-${target}`,
  source,
  target,
  ...(handle && { sourceHandle: handle }),
  type: 'smoothstep',
  animated: true,
  style: { stroke: '#6366f1', strokeWidth: 2 },
